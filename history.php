                Назвали изделие следующим образом: «Усовершенствованная игрушка или устройство для обучения детей и
                подростков». Вскоре, новая игрушка, названная «Конструктор Меккано» (Meccano) приобрела необычайную
                популярность. А автор, быстро ставший миллионером, написал книгу о самом себе, которую озаглавил:
                «Парень, который заработал $1.000.000 на игрушке»…</p>
            <p class="text-indent">Детские конструкторы считаются универсальным учебным пособием, которое развивает
                моторику рук, мышление, память, фантазию, а также множество других навыков, которые пригодятся ребенку
                в будущем. Известно, что уровень развития ребенка находится в прямой зависимости от степени
                сформированности тонких движений пальцев рук.</p>
