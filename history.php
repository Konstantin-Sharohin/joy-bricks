                который придумал Ол Кирк Кристиансен (Ole Kirk Christiansen) в 1949 году. В 1963 году для изготовления
                конструктора Лего стал использоваться пластик. Сегодня Лего - один из наиболее популярных в мире
                конструкторов, и так же как и Меккано предполагает варианты для детей разных возрастов с разными
                увлечениями.</p>
